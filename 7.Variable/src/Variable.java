public class Variable {
    public static void main(String[] args) {
        // variabel adalah tempat menyimpan data sementara
        // itu static type.
        // artinya sebuah variabel hanya dapat digunakan untuk menyimpan tipe data yang sama
        // untuk membuat variable pertama kita memasukan nama tipe data di ikuti dengan variable
        int angka; //deklarasi
        // nama variable tidak boleh menggunakan whiteSpace (spasi, enter, tab)
        //dan tidak boleh seluruhnya angka bisa kata di ikuti dengan angka ingat tdak ada whitespace

        //code program
        String name = "Minoritas Profesional"; // inisialisasi
        int age; // deklarasi
        age = 20;
        System.out.println("nama saya " + name);
        System.out.println("umur saya " + age);
        //untuk merubah variable
        age = 27;
        System.out.println("umur saya " + age);

    }
}
