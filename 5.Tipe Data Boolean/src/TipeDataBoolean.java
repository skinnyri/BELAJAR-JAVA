public class TipeDataBoolean {
    public static void main(String[] args) {
        //Tipe Data Boolean
        //tipe data boolean adalah tipe data yang memiliki dua nilai yaitu benar dan salah
        //atau nilainya hanya true dan false
        //tipe data boolean di reprentasikan dengan kata kunci boolean
        //default value boolean adalah false
        //atau jika anda tdak memasukan nilai maka nialinya secara outomatis false

        boolean benar = true;
        System.out.println(benar);
        boolean salah = false;
        System.out.println(salah);

        //walaupun tipe data ini sederhana. hampir di semua program kita pake tipe data ini
        boolean value = 1>2;
        System.out.println(value);
        //ini hanya gambaran di mana kita akan pake dia di dalam selelksi kondisi
    }
}
